package entity;

public class Curso {
    private int idCurso;
    private String tipoCurso;
    private String modalidade;
    private String denominacao;
    private String habilitacao;
    private String localOferta;
    private String turnosFuncionamento;
    private String numeroVagas;
    private String cargaHoraria;
    private String regime;
    private String periodos;

    public int getIdCurso() {
        return idCurso;
    }

    public void setIdCurso(int idCurso) {
        this.idCurso = idCurso;
    }

    public String getTipoCurso() {
        return tipoCurso;
    }

    public void setTipoCurso(String tipoCurso) {
        this.tipoCurso = tipoCurso;
    }

    public String getModalidade() {
        return modalidade;
    }

    public void setModalidade(String modalidade) {
        this.modalidade = modalidade;
    }

    public String getDenominacao() {
        return denominacao;
    }

    public void setDenominacao(String denominacao) {
        this.denominacao = denominacao;
    }

    public String getHabilitacao() {
        return habilitacao;
    }

    public void setHabilitacao(String habilitacao) {
        this.habilitacao = habilitacao;
    }

    public String getLocalOferta() {
        return localOferta;
    }

    public void setLocalOferta(String localOferta) {
        this.localOferta = localOferta;
    }

    public String getTurnosFuncionamento() {
        return turnosFuncionamento;
    }

    public void setTurnosFuncionamento(String turnosFuncionamento) {
        this.turnosFuncionamento = turnosFuncionamento;
    }

    public String getNumeroVagas() {
        return numeroVagas;
    }

    public void setNumeroVagas(String numeroVagas) {
        this.numeroVagas = numeroVagas;
    }

    public String getCargaHoraria() {
        return cargaHoraria;
    }

    public void setCargaHoraria(String cargaHoraria) {
        this.cargaHoraria = cargaHoraria;
    }

    public String getRegime() {
        return regime;
    }

    public void setRegime(String regime) {
        this.regime = regime;
    }

    public String getPeriodos() {
        return periodos;
    }

    public void setPeriodos(String periodos) {
        this.periodos = periodos;
    }
    
}
